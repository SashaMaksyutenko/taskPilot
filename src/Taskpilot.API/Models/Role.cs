namespace Taskpilot.API.Models;

/// <summary>
/// Ролі користувача в системі (RBAC — контроль доступу на основі ролей).
/// Визначає, які дії дозволені користувачу.
/// </summary>
public enum Role
{
    /// <summary>Розробник: власні задачі, чат, файли, форум, маркетплейс.</summary>
    Developer = 0,

    /// <summary>Менеджер: усе, що Developer, + проєкти, призначення задач, аналітика.</summary>
    Manager = 1,

    /// <summary>Адміністратор: повний доступ — користувачі, дані, налаштування, модерація.</summary>
    Admin = 2,

    /// <summary>Глядач: лише читання (без створення/редагування/чату).</summary>
    Viewer = 3
}
